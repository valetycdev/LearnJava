package Learn;

public class HelloWorld {
    /* Repeat it 10 times using while */
    public static void main(String[] args) {
        /* While Loop */
        int i = 0;
        while (i < 2) {
            System.out.println("Hello World!");
            i++; /* i+=1 */
        /* For Loop */
        for (int j = 0; j < 2; j++) { /* Se define (variable; condicion; incremento) */
            System.out.println("Hello World!");
        }

        }
    }


}
